char	*ft_strrchr(const char *s, int c)
{
    char	*d = 0;

    while (*s != '\0')
    {
        if (*s == (char)c)
            d = (char *)s;
        s++;
    }

    if ((char)c == '\0')
        return ((char *)s);

    return (d);
}
